namespace app\helper;
class Comm{


	static function admin_menu(){
		$m = config('app.menu');
        foreach($m as $k=>$v){
            if(strpos($v,'/')===false){
                    $m[$k] = field_url_index($v);
            }else{
                $m[$k] = url($v);
            }
        }
        return $m;
	}
}